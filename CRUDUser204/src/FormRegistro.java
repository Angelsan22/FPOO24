    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        //1. Guardamos en variables lo que escribe el usuario
        String nom = txtNombre.getText();
        String cor = txtCorreo.getText();
        String con = txtcontrasena.getText();
                
        // 2. Validamos que los campos no esten vacios
                if(nom.isEmpty() || cor.isEmpty() || con.isEmpty()){
                     JOptionPane.showMessageDialog( this, "Todos los campos son obligatorios","Error", JOptionPane.INFORMATION_MESSAGE);
                     return;
                }
                
        //3. Procedemos a INSERTAR
        
        UserCRUD crud = new UserCRUD();
        boolean status = crud.InsertUsuario(nom, cor, con);
        
        //4. Avisamos el status del INSERT al usuario
        if (status){
            JOptionPane.showMessageDialog( this, "Usuario guardado con exito en la base de datos","Registro exitoso", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog( this, "No se guardo","Error", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed
                                
